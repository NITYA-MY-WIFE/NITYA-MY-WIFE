module.exports.onLoad = ({ api }) => {
    console.log(chalk.bold.hex("#00c300")("============ SUCCESFULLY LOADED THE AUTOSENT COMMAND ============"));

    messages.forEach(({ time, message }) => {
        const [hour, minute, period] = time.split(/[: ]/);
        let hour24 = parseInt(hour, 10);
        if (period === 'PM' && hour !== '12') {
            hour24 += 12;
        } else if (period === 'AM' && hour === '12') {
            hour24 = 0;
        }

        const scheduledTime = moment.tz({ hour: hour24, minute: parseInt(minute, 10) }, 'Asia/Kolkata').toDate();

        schedule.scheduleJob(scheduledTime, () => {
            // Get all supported media files from autosend folder
            const mediaFolder = path.join(__dirname, 'autosend');
            const supportedExtensions = ['.png', '.jpg', '.jpeg', '.gif', '.webp'];
            const files = fs.readdirSync(mediaFolder).filter(file => supportedExtensions.includes(path.extname(file).toLowerCase()));

            // Select random image
            let randomImage = null;
            if (files.length > 0) {
                const randomFile = files[Math.floor(Math.random() * files.length)];
                randomImage = fs.createReadStream(path.join(mediaFolder, randomFile));
            }

            global.data.allThreadID.forEach(threadID => {
                api.sendMessage({
                    body: message,
                    attachment: randomImage || undefined
                }, threadID, (error) => {
                    if (error) {
                        console.error(`Failed to send message to ${threadID}:`, error);
                    }
                });
            });
        });
    });
};

